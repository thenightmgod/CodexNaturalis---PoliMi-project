package it.polimi.ingsw.View.GUI.GUIController.ScoreBoard;

public class ScoreBoardPosition {
    private double x;
    private double y;

    public ScoreBoardPosition(double x, double y) {
        this.x = x;
        this.y = y;
    }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }
}
