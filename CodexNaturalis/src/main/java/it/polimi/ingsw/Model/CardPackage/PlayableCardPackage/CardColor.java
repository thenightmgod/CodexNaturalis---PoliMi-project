package it.polimi.ingsw.Model.CardPackage.PlayableCardPackage;

/**
 * Enumerates the four possible colors the cards can be.
 */
public enum CardColor {
    /**
     * Red is associated with fungi kingdom.
     */
    RED,
    /**
     * Purple is associated with insect kingdom.
     */
    PURPLE,
    /**
     * Green is associated with plant kingdom.
     */
    GREEN,
    /**
     * Blue is associated with animal kingdom.
     */
    BLUE
}
