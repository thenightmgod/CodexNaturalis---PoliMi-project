package it.polimi.ingsw.Model.situaCorner;

/**
 * Represents the orientation of a corner on a card.
 */
public enum Orientation {
    /**
     * Represents the orientation of the top right corner.
     */
    HR,

    /**
     * Represents the orientation of the top left corner.
     */
    HL,

    /**
     * Represents the orientation of the bottom right corner.
     */
    LR,

    /**
     * Represents the orientation of the bottom left corner.
     */
    LL
}